#include "libft.h"

void	ft_lstdel(t_list **alst, void (*del)(void *, size_t))
{
	t_list	*list;
	t_list	*next_el;

	if (!alst || !del)
		return ;
	list = *alst;
	while (list)
	{
		next_el = list->next;
		(*del)(list->content, list->content_size);
		free(list);
		list = next_el;
	}
	*alst = NULL;
}
