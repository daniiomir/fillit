#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	unsigned char	*array;

	if (n + 1 <= 0 || n + 1 < n)
		return ;
	array = (unsigned char *)s;
	while (n != 0)
	{
		*array++ = 0;
		n--;
	}
}
